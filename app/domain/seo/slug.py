"""Транслитерация кириллицы (казахская + русская) в латиницу и построение URL-slug.

Slug нужен ЧЕЛОВЕКО- и поисково-читаемого вида: `/m/42-shrek-qazaqsha`. Ведущий id —
единственный источник правды (по нему ищем фильм), латинский хвост — только для людей и
ключевых слов в URL. Поэтому неточность транслитерации не критична: даже пустой хвост
(например, у чисто иероглифического названия) оставит рабочий `/m/42`.
"""

from __future__ import annotations

import re

# Казахские + русские буквы → латиница. Порядок не важен (dict по символу).
# Казахские специфичные (ә/ғ/қ/ң/ө/ұ/ү/і/һ) идут вместе с русскими — один проход.
_TRANSLIT: dict[str, str] = {
    "а": "a", "ә": "a", "б": "b", "в": "v", "г": "g", "ғ": "g", "д": "d",
    "е": "e", "ё": "e", "ж": "zh", "з": "z", "и": "i", "й": "i", "к": "k",
    "қ": "q", "л": "l", "м": "m", "н": "n", "ң": "ng", "о": "o", "ө": "o",
    "п": "p", "р": "r", "с": "s", "т": "t", "у": "u", "ұ": "u", "ү": "u",
    "ф": "f", "х": "h", "һ": "h", "ц": "ts", "ч": "ch", "ш": "sh", "щ": "shch",
    "ъ": "", "ы": "y", "і": "i", "ь": "", "э": "e", "ю": "yu", "я": "ya",
}

_NON_SLUG = re.compile(r"[^a-z0-9]+")
_SLUG_MAX = 60  # хвост slug'а не раздуваем — ведущий id и так уникален


def transliterate(text: str) -> str:
    """Кириллица (kk/ru) → латиница посимвольно; латиница/цифры проходят как есть."""
    out: list[str] = []
    for ch in text.lower():
        out.append(_TRANSLIT.get(ch, ch))
    return "".join(out)


def slugify(text: str) -> str:
    """Строка → URL-slug: транслит → латиница, всё прочее → дефис, схлопнуть и обрезать."""
    latin = transliterate(text)
    slug = _NON_SLUG.sub("-", latin).strip("-")
    return slug[:_SLUG_MAX].strip("-")


def movie_slug(movie_id: int, title: str) -> str:
    """URL-хвост фильма: `<id>-<slug>` (или просто `<id>`, если хвост пуст)."""
    tail = slugify(title)
    return f"{movie_id}-{tail}" if tail else str(movie_id)
