"""Выданное подписчику видео-сообщение (ссылка для последующего удаления).

Видео уходит в личку с `protect_content`, но файл всё равно оседает в чате. Чтобы
«оплатил → посмотрел → пользуюсь вечно» не работало, каждую выдачу запоминаем
(chat_id + message_id) и потом удаляем — двумя путями (`VideoRetentionService`):
  • по ВОЗРАСТУ, ежечасно (выдача живёт ~40 ч) — главный механизм;
  • сразу при истечении подписки (`SubscriptionService.expire_due`).

Почему по возрасту: Telegram НЕ даёт боту удалить сообщение старше 48 часов, поэтому
«удалим всё, когда кончится подписка» на месячном тарифе физически не работало бы.
Держим выдачи заведомо внутри окна — тогда удаление всегда успевает.

`id` — ключ строки в БД: нужен, чтобы вычищать разобранную пачку точечно.
`attempts` — сколько раз удаление сорвалось ВРЕМЕННЫМ сбоем Telegram (сеть/5xx): по нему
сервис решает, пробовать ещё или сдаться и снести строку.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class VideoDelivery:
    chat_id: int
    message_id: int
    id: int = 0        # 0 — «не из БД» (в тестах/при добавлении id ещё не присвоен)
    attempts: int = 0  # неудачных попыток удаления (только временные сбои)
